package com.zxsoft.crawler.p.occupancy;

public class SearchEngine {

	private String url;
	private String listdom, linedom, urldom;
	private String datedom, synopsisdom, updatedom, authordom;
	private String sourceName, type;
	private Integer sourceId, sectionId;
	private String keywordEncode, keyword;

	public String getUrl() {
		return url;
	}

	public void setUrl(String url) {
		this.url = url;
	}

	public String getListdom() {
		return listdom;
	}

	public void setListdom(String listdom) {
		this.listdom = listdom;
	}

	public String getLinedom() {
		return linedom;
	}

	public void setLinedom(String linedom) {
		this.linedom = linedom;
	}

	public String getUrldom() {
		return urldom;
	}

	public void setUrldom(String urldom) {
		this.urldom = urldom;
	}

	public String getDatedom() {
		return datedom;
	}

	public void setDatedom(String datedom) {
		this.datedom = datedom;
	}

	public String getSynopsisdom() {
		return synopsisdom;
	}

	public void setSynopsisdom(String synopsisdom) {
		this.synopsisdom = synopsisdom;
	}

	public String getUpdatedom() {
		return updatedom;
	}

	public void setUpdatedom(String updatedom) {
		this.updatedom = updatedom;
	}

	public String getAuthordom() {
		return authordom;
	}

	public void setAuthordom(String authordom) {
		this.authordom = authordom;
	}

	public String getSourceName() {
		return sourceName;
	}

	public void setSourceName(String sourceName) {
		this.sourceName = sourceName;
	}

	public String getType() {
		return type;
	}

	public void setType(String type) {
		this.type = type;
	}

	public Integer getSourceId() {
		return sourceId;
	}

	public void setSourceId(Integer sourceId) {
		this.sourceId = sourceId;
	}

	public Integer getSectionId() {
		return sectionId;
	}

	public void setSectionId(Integer sectionId) {
		this.sectionId = sectionId;
	}

	public String getKeywordEncode() {
		return keywordEncode;
	}

	public void setKeywordEncode(String keywordEncode) {
		this.keywordEncode = keywordEncode;
	}

	public String getKeyword() {
		return keyword;
	}

	public void setKeyword(String keyword) {
		this.keyword = keyword;
	}

}
